<?php

namespace App\Filament\Dashboard\Resources\Transactions;

use App\Filament\Dashboard\Resources\Transactions\Pages\CreateTransaction;
use App\Filament\Dashboard\Resources\Transactions\Pages\EditTransaction;
use App\Filament\Dashboard\Resources\Transactions\Pages\ListTransactions;
use App\Filament\Dashboard\Resources\Transactions\Schemas\TransactionForm;
use App\Filament\Dashboard\Resources\Transactions\Tables\TransactionsTable;
use App\Models\Transaction;
use BackedEnum;
use Filament\Resources\Resource;
use Filament\Schemas\Schema;
use Filament\Support\Icons\Heroicon;
use Filament\Tables\Table;

class TransactionResource extends Resource
{
    protected static ?string $model = Transaction::class;

    protected static string|BackedEnum|null $navigationIcon = Heroicon::OutlinedRectangleStack;

    protected static ?string $recordTitleAttribute = 'description';

    public static function form(Schema $schema): Schema
    {
        return TransactionForm::configure($schema);
    }

    public static function table(Table $table): Table
    {
        return TransactionsTable::configure($table);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => ListTransactions::route('/'),
        ];
    }

    public static function getEloquentQuery(): \Illuminate\Database\Eloquent\Builder
    {
        return parent::getEloquentQuery()->where('user_id', auth()->id());
    }

}
